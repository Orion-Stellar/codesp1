using System;

class puertasecreta
{
    static void Main()
    {
        Console.WriteLine("la puerta secreta");

        Console.WriteLine("primera puerta: escribe la letra x para abrirla");
        Console.Write("escribe una letra: ");
        string letra = Console.ReadLine();

        if (letra == "x")
        {
            Console.WriteLine("la puerta 1 se abre");
        }
        else
        {
            Console.WriteLine("no se abre la puerta 1");
            return;
        }

        Console.WriteLine("segunda puerta: escribe la palabra magica");
        Console.Write("palabra magica: ");
        string palabra = Console.ReadLine();

        if (palabra == "abracadabra")
        {
            Console.WriteLine("la puerta 2 se abre");
        }
        else
        {
            Console.WriteLine("no se abre la puerta 2");
            return;
        }

        Console.WriteLine("tercera puerta: pon un numero mayor a 10 y que sea multiplo de 3");
        Console.Write("escribe el numero: ");
        int numero = Convert.ToInt32(Console.ReadLine());

        if (numero > 10 && numero % 3 == 0)
        {
            Console.WriteLine("la puerta 3 se abre. ganaste");
        }
        else
        {
            Console.WriteLine("no se abre la puerta 3");
        }
    }
}
